package com.huawei.smartcampus.datatool.monitor.application.service.vo.alarmdata;

import org.hibernate.validator.constraints.Range;

import javax.validation.constraints.Min;

/**
 * 告警数据详情查询请求
 *
 * @author j00826364
 * @version [Campus Core 23.0, 2023/5/16]
 * @see [相关类/方法]
 * @since [Campus Core 23.0]
 */
public class AlarmDataDetailQueryRequest {
    @Range(min = 1, max = 1000, message = "{DATATOOL_PAGESIZE_INVALID}")
    private int pageSize = 10;

    @Min(value = 1, message = "{DATATOOL_PAGEINDEX_INVALID}")
    private int pageIndex = 1;

    private String jobName;

    private String alarmType;

    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }

    public int getPageIndex() {
        return pageIndex;
    }

    public void setPageIndex(int pageIndex) {
        this.pageIndex = pageIndex;
    }

    public String getJobName() {
        return jobName;
    }

    public void setJobName(String jobName) {
        this.jobName = jobName;
    }

    public String getAlarmType() {
        return alarmType;
    }

    public void setAlarmType(String alarmType) {
        this.alarmType = alarmType;
    }
}