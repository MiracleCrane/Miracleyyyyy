package com.huawei.smartcampus.datatool.stream.mgt.vo;

import com.huawei.smartcampus.datatool.enums.JobStatus;

/**
 * 作业状态信息
 *
 * @author s30009470
 * @version [SmartCampus V100R001C00, 2021/2/10]
 * @see [相关类/方法]
 * @since [SmartCampus V100R001C00]
 */

public class JobStatusInfo {
    private String id;
    private JobStatus status;

    /**
     * 无参构造
     */
    public JobStatusInfo() {
    }

    public JobStatusInfo(String id, JobStatus status) {
        this.id = id;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public JobStatus getStatus() {
        return status;
    }

    public void setStatus(JobStatus status) {
        this.status = status;
    }
}
