public class FlinkRunErrorInfo {
    private String name;
    private String msg;

    /**
     * 无参构造
     */
    public FlinkRunErrorInfo() {
    }

    public FlinkRunErrorInfo(String name, String msg) {
        this.name = name;
        this.msg = msg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
